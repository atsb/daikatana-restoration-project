///////////////////////////////////////////////////////////////////////////////
//	dk_SetResourcePath
///////////////////////////////////////////////////////////////////////////////

void	dk_SetResourcePath (char *path)
{
	//	copy it over to our global array
// SCG[1/16/00]: 	sprintf (dk_resourcePath, "%s", path);
	Com_sprintf (dk_resourcePath,sizeof(dk_resourcePath), "%s", path);

	//	set cvar for rendering DLLs
	if (re.SetResourceDir)
		re.SetResourceDir (dk_resourcePath);
}

///////////////////////////////////////////////////////////////////////////////
//	dk_InitResourcePath
//
//	Called when Daikatana first initializes
///////////////////////////////////////////////////////////////////////////////

void	dk_InitResourcePath (void)
{
	//	copy it over to our global array
	dk_SetResourcePath ("");
}